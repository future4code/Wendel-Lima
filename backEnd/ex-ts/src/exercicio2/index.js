function adicao(a, b) {
    return a + b;
}
var resultAdicao = adicao(7, 3);
console.log(resultAdicao);
function subtracao(a, b) {
    return a - b;
}
var resultSubtracao = subtracao(9, 4);
console.log(resultSubtracao);
function multiplicacao(a, b) {
    return a * b;
}
var resultMultiplicacao = multiplicacao(3, 5);
console.log(resultMultiplicacao);
function maiorQue(a, b) {
    return a > b ? a : b;
}
var resultMaiorQue = maiorQue(12, 5);
console.log(resultMaiorQue);
