function adicao(a: number, b: number): number {
    return a + b
}
    const resultAdicao = adicao(7, 3);
    console.log(resultAdicao);

function subtracao (a: number, b: number): number {
    return a - b
}
    const resultSubtracao = subtracao(9, 4);
    console.log(resultSubtracao);

function multiplicacao(a: number, b: number): number {
    return a * b
}
    const resultMultiplicacao = multiplicacao(3, 5)
    console.log(resultMultiplicacao);

function maiorQue(a: number, b: number): number {
    return a > b ? a : b
}
    const resultMaiorQue = maiorQue (12, 5)
    console.log(resultMaiorQue);

